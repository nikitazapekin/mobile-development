package com.example.lab_6;
public class Work {
    private String title;
    private String releaseDate;

    public Work(String title, String releaseDate) {
        this.title = title;
        this.releaseDate = releaseDate;
    }

    public String getTitle() {
        return title;
    }

    public String getReleaseDate() {
        return releaseDate;
    }
}

